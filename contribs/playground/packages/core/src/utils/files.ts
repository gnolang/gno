import { derivePackageStructure } from '../services'
import type { PlainFile } from '../types'

const gnoExtensionRegEx = /(\.gno)$/i
const METADATA_FILENAME = 'gnostudio.gno'
const MOD_FILENAME = 'gnomod.toml'

const reservedFileNames = [METADATA_FILENAME]

export function isReservedFileName(path: string) {
  return reservedFileNames.includes(path)
}

export function filterValidFiles(files: PlainFile[]): PlainFile[] {
  return files.filter((file) => !isReservedFileName(file.path))
}

export function injectStudioMetaFiles(files: PlainFile[], pkgPath: string): PlainFile[] {
  const gnoFiles = files.filter((file) => gnoExtensionRegEx.test(file.path))
  if (!gnoFiles.length) return files

  const pkgName = inferPackageNameFromText(gnoFiles[0].content)
  if (!pkgName) return files

  const newFiles = [...files]
  const hasMetaFile = files.some((file) => file.path === METADATA_FILENAME)
  const hasModFile = files.some((file) => file.path === MOD_FILENAME)

  if (!hasMetaFile) {
    const metaContent = generateMetaContent(pkgName)
    newFiles.push({
      path: METADATA_FILENAME,
      content: metaContent,
    })
  }

  if (!hasModFile) {
    const modContent = generateModFileContent(pkgPath)
    newFiles.push({
      path: MOD_FILENAME,
      content: modContent,
    })
  }

  return newFiles
}

function generateMetaContent(packageName: string) {
  return deindent(`
    // This file is automatically generated and contains metadata about this
    // package deployment. It serves as a marker that this package was deployed
    // using Gno Studio (or Gno Playground) and may be used by tools for analytics, verification,
    // and ecosystem features.
    //
    // This file does not affect package execution and should not be modified.
    package ${packageName}`)
}

function generateModFileContent(pkgPath: string) {
  const modFile = `module = "${pkgPath}"\ngno = "0.9"\n`
  return modFile
}

function inferPackageNameFromText(code: string) {
  const { package: pkgName } = derivePackageStructure(code)
  if (!pkgName) return

  return pkgName
}

function deindent(text: string) {
  const lines = text.trim().split('\n')
  const indentedLines = lines.map((line) => line.trim())
  return indentedLines.join('\n')
}
