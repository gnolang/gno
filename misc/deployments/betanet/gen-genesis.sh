#!/usr/bin/env bash
# Generate betanet genesis.json.
set -e

# ---- Config

CHAIN_ID=betanet
GENESIS_TIME=1770883200 # Thursday, February 12th 2026 09:00 GMT+0100 (Central European Standard Time)
DEPLOYER_MNEMONIC="anchor hurt name seed oak spread anchor filter lesson shaft wasp home improve text behind toe segment lamp turn marriage female royal twice wealth"
BALANCES_FILE=./genesis_balances.txt
GENESIS_FILE=./genesis.json
WORK_DIR=./genesis-work

FILTERED_PACKAGES=(
  ./gno.land/r/sys/...
  ./gno.land/r/gov/...
  ./gno.land/r/gnoland/blog/...
  ./gno.land/r/gnoland/wugnot/...
  ./gno.land/r/gnoland/coins/...
  ./gno.land/r/gnoland/boards2/...
  ./gno.land/r/gnops/valopers/...
)

INITIAL_VALSET=(
  # Values: "name power address pub_key"
  "gnocore-val-01 1 g1e8j30772p4fvjd27eqq0e50vs74ekst50f5hz6 gpub1pgfj7ard9eg82cjtv4u4xetrwqer2dntxyfzxz3pqdw9mje8pwpd8p6pu3h0ke9mxp3s7l5vr6jtsffyd9wazpgxufwrssj367n"
  "gnocore-val-02 1 g18k5nz67cg03lrdkwnf9qmdfzf0ptaq7lxefe4y gpub1pgfj7ard9eg82cjtv4u4xetrwqer2dntxyfzxz3pq0mxx3hhmy5hsl9kv8ne2ch46l8uk3kpnwc8kr900066ejz3zlhqcj6wjna"
)

UNRESTRICTED_ADDRESSES=(
  g148583t5x66zs6p90ehad6l4qefeyaf54s69wql
  g1manfred47kzduec920z88wfr64ylksmdcedlf5
)

# ---- Internal (do not edit)

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "$SCRIPT_DIR/../../.." && pwd)"
EXAMPLES_DIR="$REPO_ROOT/examples"
GNO_CMD="$REPO_ROOT/gnovm/cmd/gno"
GNOKEY_CMD="$REPO_ROOT/gno.land/cmd/gnokey"
GNOLAND_CMD="$REPO_ROOT/gno.land/cmd/gnoland"
GNOGENESIS_CMD="$REPO_ROOT/contribs/gnogenesis"
WORK_DIR_BIN="$WORK_DIR/bin"
GNO_BIN="$WORK_DIR_BIN/gno"
GNOKEY_BIN="$WORK_DIR_BIN/gnokey"
GNOLAND_BIN="$WORK_DIR_BIN/gnoland"
GNOGENESIS_BIN="$WORK_DIR_BIN/gnogenesis"

# ---- 1. Build binaries from source to ensure we have the right versions.

echo "Building binaries from source..."

mkdir -p "$WORK_DIR_BIN"

go build -C "$GNO_CMD" -o "$GNO_BIN" .
go build -C "$GNOKEY_CMD" -o "$GNOKEY_BIN" .
go build -C "$GNOLAND_CMD" -o "$GNOLAND_BIN" .
go build -C "$GNOGENESIS_CMD" -o "$GNOGENESIS_BIN" .

# ---- 2. Generate filtered examples genesis txs.

echo "Generating genesis addpkg txs for filtered examples...\n${FILTERED_PACKAGES[@]}"

# Resolve listed realms + all transitive deps in topological order.
pkg_dirs=$(cd "$EXAMPLES_DIR" && "$GNO_BIN" tool deplist "${FILTERED_PACKAGES[@]}")

# Save resolved package list for inspection.
{
  echo "# Generated by gen-genesis.sh — do not edit."
  echo "$pkg_dirs" | sed "s|$EXAMPLES_DIR/||g"
} >"$SCRIPT_DIR/packages.gen.txt"

# Copy resolved packages into the working directory.
WORK_DIR_EXAMPLES="$WORK_DIR/examples"
mkdir -p "$WORK_DIR_EXAMPLES"
while IFS= read -r dir; do
  [[ -z "$dir" ]] && continue
  rel="${dir#$EXAMPLES_DIR/}"
  mkdir -p "$(dirname "$WORK_DIR_EXAMPLES/$rel")"
  cp -r "$dir" "$WORK_DIR_EXAMPLES/$rel"
done <<<"$pkg_dirs"

# Strip test files — they pull in deps (like uassert) not needed in genesis.
find "$WORK_DIR_EXAMPLES" -name '*_test.gno' -delete
find "$WORK_DIR_EXAMPLES" -name '*_filetest.gno' -delete

# Export genesis txs for filtered packages.
WORK_DIR_GNOKEY_HOME="$WORK_DIR/gnokey-home"
WORK_DIR_GENESIS="$WORK_DIR/genesis.json"
WORK_DIR_GENESIS_TXS="$WORK_DIR/genesis_txs.jsonl"
echo "$DEPLOYER_MNEMONIC\n" "$GNOKEY_BIN" add --recover GenesisDeployer --home "$WORK_DIR_GNOKEY_HOME" -insecure-password-stdin
"$GNOGENESIS_BIN" generate -chain-id "$CHAIN_ID" -genesis-time "$GENESIS_TIME" --output-path "$WORK_DIR_GENESIS"
"$GNOGENESIS_BIN" txs add packages "$WORK_DIR_EXAMPLES" -gno-home "$WORK_DIR_GNOKEY_HOME" -key-name GenesisDeployer --genesis-path "$WORK_DIR_GENESIS"
"$GNOGENESIS_BIN" txs export "$WORK_DIR_GENESIS_TXS" --genesis-path "$WORK_DIR_GENESIS"

# ---- 3. Generate setup transaction (initial validator set, etc.)

echo "Generating genesis run tx for initial setup..."

# TODO:
# - Remove the list of members hardcoded in the loader.gno file in the govdao realm
# - Add the genesis_deployer key as the unique initial member of the govdao realm
# - Write a gnolang file which:
#   1. create an initial govdao prop which:
#     - Add the govdao members
#     - Add the initial validator set
#     - Remove the genesis_deployer from the govdao members
#   2. vote and execute the prop
# - Add a GOVDAO_MEMBERS variable to the config of the current bash script
# - Sed the gnolang file to add the values from INITIAL_VALSET and GOVDAO_MEMBERS
# - Generate a tx using `gnokey maketx run` that run the gnolang file
# - Append the generated tx to a genesis_setup_tx.jsonl file, then run:
#   "$GNOGENESIS_BIN" txs add sheets genesis_setup_tx.jsonl --genesis-path "$WORK_DIR_GENESIS"

# ---- 4. Calculate the deployers balances

echo "Generating genesis balance sheet for deployers..."

WORK_DIR_GENESIS_BALANCES="$WORK_DIR/genesis_balances.txt"
BALANCES_TMP_DIR="$WORK_DIR/balances-work"
BALANCES_TMP_FILE="$BALANCES_TMP_DIR/balances.txt"
BALANCES_TMP_GNOLAND_DATA="$BALANCES_TMP_DIR/gnoland-data"
BALANCES_TMP_GNOLAND_LOG="$BALANCES_TMP_DIR/node.log"
BALANCES_TMP_GENESIS="$BALANCES_TMP_DIR/genesis.json"
BALANCES_TMP_CREATOR_ADDRESSES="$BALANCES_TMP_DIR/gen-creators.txt"
INITIAL_BALANCE=1000000000000000
NODE_TIMEOUT=120

# Extract and deduplicate creator addresses

rm -rf "$BALANCES_TMP_DIR"
mkdir -p "$BALANCES_TMP_DIR"

grep -o '"creator":"[^"]*"' "$WORK_DIR_GENESIS_TXS" |
  sed 's/"creator":"//;s/"//' |
  sort -u >"$BALANCES_TMP_CREATOR_ADDRESSES"

# Generate over provisionned balances file

while IFS= read -r addr; do
  echo "${addr}=${INITIAL_BALANCE}ugnot" >>"$BALANCES_TMP_FILE"
done <"$BALANCES_TMP_CREATOR_ADDRESSES"

# Set up gnoland node working directory

"$GNOGENESIS_BIN" generate -chain-id "$CHAIN_ID" -genesis-time "$(date +%s)" -output-path "$BALANCES_TMP_GENESIS"
"$GNOGENESIS_BIN" txs add sheets "$WORK_DIR_GENESIS_TXS" -genesis-path "$BALANCES_TMP_GENESIS"
"$GNOGENESIS_BIN" balances add -balance-sheet "$BALANCES_TMP_FILE" -genesis-path "$BALANCES_TMP_GENESIS"
"$GNOLAND_BIN" config init -config-path "$BALANCES_TMP_GNOLAND_DATA/config/config.toml"
"$GNOLAND_BIN" secrets init -data-dir "$BALANCES_TMP_GNOLAND_DATA/secrets"
"$GNOGENESIS_BIN" validator add \
  --address "$("$GNOLAND_BIN" secrets get validator_key.address --raw -data-dir "$BALANCES_TMP_GNOLAND_DATA/secrets")" \
  --pub-key "$("$GNOLAND_BIN" secrets get validator_key.pub_key --raw -data-dir "$BALANCES_TMP_GNOLAND_DATA/secrets")" \
  --name balance_generator \
  --power 1 \
  -genesis-path "$BALANCES_TMP_GENESIS"

# Start the node (first run) and wait for "This node is a validator"

"$GNOLAND_BIN" start --skip-genesis-sig-verification -data-dir "$BALANCES_TMP_GNOLAND_DATA" -genesis "$BALANCES_TMP_GENESIS" >"$BALANCES_TMP_GNOLAND_LOG" 2>&1 &
NODE_PID=$!

elapsed=0
node_started=false
while [ "$elapsed" -lt "$NODE_TIMEOUT" ]; do
  if ! kill -0 "$NODE_PID" 2>/dev/null; then
    echo "ERROR: Node stopped unexpectedly. Last log lines:"
    tail -20 "$BALANCES_TMP_GNOLAND_LOG"
    exit 1
  fi
  if grep -q "This node is a validator" "$BALANCES_TMP_GNOLAND_LOG" 2>/dev/null; then
    node_started=true
    break
  fi
  sleep 1
  elapsed=$((elapsed + 1))
done

if [ "$node_started" = false ]; then
  kill "$NODE_PID" 2>/dev/null || true
  echo "ERROR: Node did not start within ${NODE_TIMEOUT}s. Last log lines:"
  tail -20 "$BALANCES_TMP_GNOLAND_LOG"
  exit 1
fi

# Query remaining balances and compute final balances

rm -f "$BALANCES_TMP_FILE"
while IFS= read -r addr; do
  remaining=""
  retry=0
  while [ "$retry" -lt "$NODE_TIMEOUT" ]; do
    if ! kill -0 "$NODE_PID" 2>/dev/null; then
      echo "ERROR: Node stopped unexpectedly during balance query. Last log lines:"
      tail -20 "$BALANCES_TMP_GNOLAND_LOG"
      exit 1
    fi
    query_output=$($GNOKEY_BIN query "bank/balances/$addr" 2>&1 || true)
    if echo "$query_output" | grep -q '^data:'; then
      remaining=$(echo "$query_output" | sed -n 's/.*"\([0-9]*\)ugnot".*/\1/p' | head -1)
      # Empty data field means 0 balance
      remaining=${remaining:-0}
      break
    fi
    sleep 1
    retry=$((retry + 1))
  done
  if [ -z "$remaining" ]; then
    echo "ERROR: Could not query balance for $addr after ${NODE_TIMEOUT}s. Last log lines:"
    tail -20 "$BALANCES_TMP_GNOLAND_LOG"
    kill "$NODE_PID" 2>/dev/null || true
    exit 1
  fi
  final=$((INITIAL_BALANCE - remaining))
  echo "${addr}=${final}ugnot" >>"$BALANCES_TMP_FILE"
done <"$BALANCES_TMP_CREATOR_ADDRESSES"

kill "$NODE_PID" 2>/dev/null || true
wait "$NODE_PID" 2>/dev/null || true

# Regenerate genesis with calculated balances

rm -rf "$BALANCES_TMP_GNOLAND_DATA" "$BALANCES_TMP_GENESIS"

"$GNOGENESIS_BIN" generate -chain-id "$CHAIN_ID" -genesis-time "$(date +%s)" -output-path "$BALANCES_TMP_GENESIS"
"$GNOGENESIS_BIN" txs add sheets "$WORK_DIR_GENESIS_TXS" -genesis-path "$BALANCES_TMP_GENESIS"
"$GNOGENESIS_BIN" balances add -balance-sheet "$BALANCES_TMP_FILE" -genesis-path "$BALANCES_TMP_GENESIS"
"$GNOLAND_BIN" config init -config-path "$BALANCES_TMP_GNOLAND_DATA/config/config.toml"
"$GNOLAND_BIN" secrets init -data-dir "$BALANCES_TMP_GNOLAND_DATA/secrets"
"$GNOGENESIS_BIN" validator add \
  --address "$("$GNOLAND_BIN" secrets get validator_key.address --raw -data-dir "$BALANCES_TMP_GNOLAND_DATA/secrets")" \
  --pub-key "$("$GNOLAND_BIN" secrets get validator_key.pub_key --raw -data-dir "$BALANCES_TMP_GNOLAND_DATA/secrets")" \
  --name balance_generator \
  --power 1 \
  -genesis-path "$BALANCES_TMP_GENESIS"

# Start the node (second run) and verify all balances are zero

"$GNOLAND_BIN" start --skip-genesis-sig-verification -data-dir "$BALANCES_TMP_GNOLAND_DATA" -genesis "$BALANCES_TMP_GENESIS" >"$BALANCES_TMP_GNOLAND_LOG" 2>&1 &
NODE_PID=$!

elapsed=0
node_started=false
while [ "$elapsed" -lt "$NODE_TIMEOUT" ]; do
  if ! kill -0 "$NODE_PID" 2>/dev/null; then
    echo "ERROR: Node stopped unexpectedly. Last log lines:"
    tail -20 "$BALANCES_TMP_GNOLAND_LOG"
    exit 1
  fi
  if grep -q "This node is a validator" "$BALANCES_TMP_GNOLAND_LOG" 2>/dev/null; then
    node_started=true
    break
  fi
  sleep 1
  elapsed=$((elapsed + 1))
done

if [ "$node_started" = false ]; then
  kill "$NODE_PID" 2>/dev/null || true
  echo "ERROR: Node did not start within ${NODE_TIMEOUT}s. Last log lines:"
  tail -20 "$BALANCES_TMP_GNOLAND_LOG"
  exit 1
fi

all_zero=true
while IFS= read -r addr; do
  remaining=""
  retry=0
  while [ "$retry" -lt "$NODE_TIMEOUT" ]; do
    if ! kill -0 "$NODE_PID" 2>/dev/null; then
      echo "ERROR: Node stopped unexpectedly during balance verification. Last log lines:"
      tail -20 "$BALANCES_TMP_GNOLAND_LOG"
      exit 1
    fi
    query_output=$($GNOKEY_BIN query "bank/balances/$addr" 2>&1 || true)
    if echo "$query_output" | grep -q '^data:'; then
      remaining=$(echo "$query_output" | sed -n 's/.*"\([0-9]*\)ugnot".*/\1/p' | head -1)
      # Empty data field means 0 balance
      remaining=${remaining:-0}
      break
    fi
    sleep 1
    retry=$((retry + 1))
  done
  if [ -z "$remaining" ]; then
    echo "ERROR: Could not query balance for $addr after ${NODE_TIMEOUT}s. Last log lines:"
    tail -20 "$BALANCES_TMP_GNOLAND_LOG"
    kill "$NODE_PID" 2>/dev/null || true
    exit 1
  fi
  if [ "$remaining" -ne 0 ]; then
    echo "WARNING: $addr has non-zero balance: ${remaining}ugnot"
    all_zero=false
  fi
done <"$BALANCES_TMP_CREATOR_ADDRESSES"

kill "$NODE_PID" 2>/dev/null || true
wait "$NODE_PID" 2>/dev/null || true

if [ "$all_zero" = true ]; then
  cp "$BALANCES_TMP_FILE" "$WORK_DIR_GENESIS_BALANCES"
else
  echo "ERROR: Some balances are not zero after replay. Check $BALANCES_TMP_FILE."
  exit 1
fi

# TODO: Append the user balances files to the deployer balance sheet

"$GNOGENESIS_BIN" balances add -balance-sheet "$WORK_DIR_GENESIS_BALANCES" --genesis-path "$WORK_DIR_GENESIS"

# ---- 5. Add the initial validator set to the genesis file

echo "Adding initial validator set to genesis..."

for validator in "${VALIDATORS[@]}"; do
  read -r name power address pub_key <<<"$validator"
  "$GNOGENESIS_BIN" validator add -name "$name" -power "$power" -address "$address" -pub-key "$pub_key" --genesis-path "$WORK_DIR_GENESIS"
done

# ---- 6. Add the required genesis parameters

echo "Adding genesis parameters..."

"$GNOGENESIS_BIN" params set auth.unrestricted_addrs "$(
  IFS=,
  echo "${UNRESTRICTED_ADDRESSES[*]}"
)" --genesis-path "$WORK_DIR_GENESIS"
"$GNOGENESIS_BIN" params set bank.restricted_denoms "ugnot" --genesis-path "$WORK_DIR_GENESIS"

# ---- 7. Verify the generated genesis file

echo "Verifying generated genesis..."

"$GNOGENESIS_BIN" verify -genesis-path "$WORK_DIR_GENESIS"

cp "$WORK_DIR_GENESIS" "$GENESIS_FILE"
echo "Done: $GENESIS_FILE (sha256: $(shasum -a 256 "$GENESIS_FILE" | awk '{print $1}'))"
