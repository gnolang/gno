package fuzzbinary

import (
	"testing"

	amino "github.com/gnolang/gno/tm2/pkg/amino"
	"github.com/gnolang/gno/tm2/pkg/amino/tests"
)

// FuzzUnmarshalBinary2 is the native Go fuzz equivalent of Fuzz (go-fuzz)
// but for genproto2's generated UnmarshalBinary2.
// (Test that deserialize never panics)
func FuzzUnmarshalBinary2(f *testing.F) {
	f.Add([]byte{})
	f.Add([]byte{0x00})
	f.Add([]byte{0x0A, 0x01, 0x42})
	// InterfaceHeavy{Concrete1, Concrete2, [Concrete1,Concrete2], "fuzz"} —
	// valid Any-encoded interfaces so the fuzzer can reach dispatch.
	f.Add([]byte{
		0xa, 0x12, 0xa, 0x10, 0x2f, 0x74, 0x65, 0x73, 0x74, 0x73, 0x2e,
		0x43, 0x6f, 0x6e, 0x63, 0x72, 0x65, 0x74, 0x65, 0x31, 0x12, 0x12,
		0xa, 0x10, 0x2f, 0x74, 0x65, 0x73, 0x74, 0x73, 0x2e, 0x43, 0x6f,
		0x6e, 0x63, 0x72, 0x65, 0x74, 0x65, 0x32, 0x22, 0x12, 0xa, 0x10,
		0x2f, 0x74, 0x65, 0x73, 0x74, 0x73, 0x2e, 0x43, 0x6f, 0x6e, 0x63,
		0x72, 0x65, 0x74, 0x65, 0x31, 0x22, 0x12, 0xa, 0x10, 0x2f, 0x74,
		0x65, 0x73, 0x74, 0x73, 0x2e, 0x43, 0x6f, 0x6e, 0x63, 0x72, 0x65,
		0x74, 0x65, 0x32, 0x2a, 0x4, 0x66, 0x75, 0x7a, 0x7a,
	})
	// GnoVMTypedValue{Concrete1, Concrete2, [8]byte{1..8}}.
	f.Add([]byte{
		0xa, 0x12, 0xa, 0x10, 0x2f, 0x74, 0x65, 0x73, 0x74, 0x73, 0x2e,
		0x43, 0x6f, 0x6e, 0x63, 0x72, 0x65, 0x74, 0x65, 0x31, 0x12, 0x12,
		0xa, 0x10, 0x2f, 0x74, 0x65, 0x73, 0x74, 0x73, 0x2e, 0x43, 0x6f,
		0x6e, 0x63, 0x72, 0x65, 0x74, 0x65, 0x32, 0x1a, 0x8, 0x1, 0x2,
		0x3, 0x4, 0x5, 0x6, 0x7, 0x8,
	})
	// AminoMarshalerStruct1{A:7, B:-3}.
	f.Add([]byte{0x8, 0xe, 0x10, 0x5})

	cdc := amino.NewCodec()
	cdc.RegisterPackage(tests.Package)
	cdc.Seal()

	f.Fuzz(func(t *testing.T, data []byte) {
		// Primitive fields only (no codec needed).
		var s tests.PrimitivesStruct
		_ = s.UnmarshalBinary2(nil, data)

		// Embedded structs, no interface fields.
		var cs tests.ComplexSt
		_ = cs.UnmarshalBinary2(cdc, data)

		// Interface-heavy types that exercise UnmarshalAnyBinary2.
		var tv tests.GnoVMTypedValue
		_ = tv.UnmarshalBinary2(cdc, data)

		var blk tests.GnoVMBlock
		_ = blk.UnmarshalBinary2(cdc, data)

		var node tests.GnoVMNode
		_ = node.UnmarshalBinary2(cdc, data)

		var ih tests.InterfaceHeavy
		_ = ih.UnmarshalBinary2(cdc, data)

		// Custom AminoMarshaler (repr type conversion path).
		var am tests.AminoMarshalerStruct1
		_ = am.UnmarshalBinary2(cdc, data)
	})
}
