package genproto2

import (
	"go/parser"
	"go/token"
	"strings"
	"testing"

	"github.com/gnolang/gno/tm2/pkg/amino"
	"github.com/gnolang/gno/tm2/pkg/amino/tests"
)

func TestGenerateProtobuf3(t *testing.T) {
	cdc := amino.NewCodec()
	cdc.RegisterPackage(tests.Package)
	cdc.Seal()

	ctx := NewP3Context2(cdc)
	rtz := tests.Package.ReflectTypes()

	src, err := ctx.GenerateProtobuf3ForTypes("tests", rtz...)
	if err != nil {
		t.Fatalf("GenerateProtobuf3ForTypes: %v", err)
	}

	// Syntactic validity: output must parse as Go.
	fset := token.NewFileSet()
	if _, err := parser.ParseFile(fset, "pb3_gen.go", src, parser.AllErrors); err != nil {
		t.Fatalf("generated source does not parse: %v\n---\n%s", err, src)
	}

	// Content sanity: a representative set of expected declarations.
	wantSubstrings := []string{
		"// Code generated by genproto2; DO NOT EDIT.",
		"package tests",
		"func (goo PrimitivesStruct) MarshalBinary2(",
		"func (goo PrimitivesStruct) SizeBinary2(cdc *amino.Codec) (int, error) {",
		"func (goo *PrimitivesStruct) UnmarshalBinary2(",
		"amino.RegisterGenproto2Type(reflect.TypeOf((*PrimitivesStruct)(nil)).Elem())",
	}
	for _, want := range wantSubstrings {
		if !strings.Contains(src, want) {
			t.Errorf("generated source missing expected substring: %q", want)
		}
	}
}
